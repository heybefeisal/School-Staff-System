    private void btnAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminActionPerformed
        if(val.textFaltHarVarde(txtAnvandare) && val.isEmailCorrect(txtAnvandare))
        try {
            String mail = txtAnvandare.getText();
            String fraga = "SELECT TYPER FROM USERS WHERE EMAIL = '" + mail + "';";
            String svar = idb.fetchSingle(fraga);
            if (svar.equals("USER")) {
                String update = "UPDATE USERS SET TYPER = 'ADMIN' WHERE EMAIL = '" + mail + "';";
                idb.update(update);
                JOptionPane.showMessageDialog(null, "Användaren är nu admin!");
            } else {
                JOptionPane.showMessageDialog(null, "Användaren är redan admin, försök med en user!");
            }
        } catch (InfException ex) {
            Logger.getLogger(AdminRedigiering.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAdminActionPerformed

